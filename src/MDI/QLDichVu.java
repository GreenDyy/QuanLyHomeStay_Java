    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        if(action.equals("them"))
        {
            if(txtMaDV.getText().equals("") || txtTenDV.getText().equals("") || txtGiaDV.getText().equals(""))
            {
                JOptionPane.showMessageDialog(this, "Vui lòng nhập đầy đủ thông tin!", "Thông báo", 2);
            }
            else
            {
                try
                {
                    String maloaidv = DichVuDAO.getMaLoaiDV(cbLoaiDV.getSelectedItem().toString()); // do là bảng dich hiển thị tên mã loại nên cần chuyển lại để insert hay update
                    
                    String lenhsql = "insert into DICHVU(MADV, TENDV, MALDV, GIADV) values('"+txtMaDV.getText().trim()+"', N'"+txtTenDV.getText().trim()+"', '"+maloaidv+"', "+txtGiaDV.getText().trim()+")";
                    int kq=DichVuDAO.ThemXoaSuaDichVu(lenhsql);
                    if(kq!=-1)
                    {
                        dsdv = DichVuDAO.getDSDV();
                        laydulieudichvu(dsdv);
                        JOptionPane.showMessageDialog(this, "Thêm thành công!", "Thông báo", 2);
                    }
                    else
                    JOptionPane.showMessageDialog(this, "Mã dịch vụ này đã tồn tại, không thể thêm", "Thông báo", 2);
                }
                catch(Exception e)
                {
                    System.out.println(e);
                }
            }

        }
        else if(action.equals("xoa"))
        {
            String sql = "delete from DichVu where MaDV = '"+txtMaDV.getText().trim()+"'";
            if(DichVuDAO.ThemXoaSuaDichVu(sql)!=-1)
            {
                dsdv = DichVuDAO.getDSDV();
                laydulieudichvu(dsdv);
                JOptionPane.showMessageDialog(this, "Xoá thành công!", "Thông báo", 2);
            }
            else
            JOptionPane.showMessageDialog(this, "Dữ liệu này đang được sử dụng, không thể xóa", "Thông báo", 2);
        }
        //sửa
        else if(action.equals("sua"))
        {
            try{
                String maloaidv = DichVuDAO.getMaLoaiDV(cbLoaiDV.getSelectedItem().toString()); // do là bảng dich hiển thị tên mã loại nên cần chuyển lại để insert hay update
                
                String sql = "UPDATE DICHVU SET TENDV = N'"+txtTenDV.getText().trim()+"', MALDV = '"+maloaidv+"', "
                        + "GIADV = "+txtGiaDV.getText().trim()+" WHERE MADV = '"+txtMaDV.getText().trim()+"'";
                DichVuDAO.ThemXoaSuaDichVu(sql);
                dsdv = DichVuDAO.getDSDV();
                laydulieudichvu(dsdv);
                JOptionPane.showMessageDialog(this, "Sửa thành công!", "Thông báo", 2);
            }
            catch(Exception e)
            {
                System.out.println(e);
            }
        }

        //set lại mặc định
        setControl();
    }//GEN-LAST:event_btnSaveActionPerformed

