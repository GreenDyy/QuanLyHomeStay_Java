    public final void loadCBLoaiDV()
    {
        cbLoaiDV.removeAllItems();
        ArrayList<String> dsLoaiDV = DichVuDAO.getLoaiDV();
        for(String loaidv : dsLoaiDV)
        {
            cbLoaiDV.addItem(loaidv);
        }
    }
    public final void bangdichvu(){
        tblTitleDV.add("Mã DV");
        tblTitleDV.add("Tên DV");
        tblTitleDV.add("Loại dịch vụ");
        tblTitleDV.add("Giá");
    }
    
    public final void laydulieudichvu(ArrayList<DichVu> dsdv){
        tblDataDV.removeAllElements();
        for(DichVu dv : dsdv){
            Vector v = new Vector();
            v.add(dv.getMadv()); 
            v.add(dv.getTendv()); 
            v.add(dv.getLoaidv());
            v.add(dv.getGiadv());
            tblDataDV.add(v);
        }
        //cách 2 
        tblModeDV = new DefaultTableModel(tblDataDV, tblTitleDV);
        tblDichVu.setModel(tblModeDV);
    }
