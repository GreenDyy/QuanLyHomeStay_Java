    public final void laydulieukhachhang(ArrayList<KhachHang> dskh){
        tblDataKH.removeAllElements();
        for(KhachHang kh : dskh){
            Vector v = new Vector();
            v.add(kh.getMAKH()); 
            v.add(kh.getTENKH()); 
            v.add(kh.getGIOITINH()); 
            v.add(kh.getNGAYSINH()); 
            v.add(kh.getDIACHI()); 
            v.add(kh.getSDT());

            tblDataKH.add(v);
        }
        tblModeKH = new DefaultTableModel(tblDataKH, tblTitleKH);
        tblKhachHang.setModel(tblModeKH);
    }
    
