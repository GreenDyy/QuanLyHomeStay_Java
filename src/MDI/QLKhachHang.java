    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        String ngaysinh = ((JTextField)dateNgaySinh.getDateEditor().getUiComponent()).getText();
        if(action.equals("them"))
        {
            if(txtMaKH.getText().equals("") || txtTenKH.getText().equals("") || txtSDT.getText().equals(""))
            {
                JOptionPane.showMessageDialog(this, "Vui lòng nhập đầy đủ thông tin!", "Thông báo", 2);
            }
            else
            {
                try
                {
                    String lenhsql = "insert into KhachHang(MaKH, TenKH, GioiTinh, NgaySinh,  DiaChi, SDT) " +
                    "values ('"+txtMaKH.getText()+"', N'"+txtTenKH.getText()+"', N'"+cbGioiTinh.getSelectedItem().toString().trim()+"', N'"+ngaysinh+"'"
                    + ", N'"+txtDiaChi.getText()+"', '"+txtSDT.getText()+"')";
                    int kq=KhachHangDAO.ThemXoaSuaKhachHang(lenhsql);
                    if(kq!=-1)
                    {
                        dskh = KhachHangDAO.getDSKH();
                        laydulieukhachhang(dskh);
                        JOptionPane.showMessageDialog(this, "Thêm thành công!", "Thông báo", 2);
                    }
                    else
                    JOptionPane.showMessageDialog(this, "Mã nhân viên này đã tồn tại, không thể thêm", "Thông báo", 2);
                }
                catch(Exception e)
                {
                    System.out.println(e);
                }
            }

        }
        else if(action.equals("xoa"))
        {
            String sql = "delete from KhachHang where MaKH = '"+txtMaKH.getText().trim()+"'";
            if(KhachHangDAO.ThemXoaSuaKhachHang(sql)!=-1)
            {
                dskh = KhachHangDAO.getDSKH();
                laydulieukhachhang(dskh);
                JOptionPane.showMessageDialog(this, "Xoá thành công!", "Thông báo", 2);
            }
            else
            JOptionPane.showMessageDialog(this, "Dữ liệu này đang được sử dụng, không thể xóa", "Thông báo", 2);
        }
        else if(action.equals("sua"))
        {
            try{
                String sql = "UPDATE KhachHang SET TenKH = N'"+txtTenKH.getText()+"', NgaySinh = '"+ngaysinh+"', GioiTinh = N'"+cbGioiTinh.getSelectedItem()+"', "
                + "DiaChi = N'"+txtDiaChi.getText()+"', SDT = '"+txtSDT.getText()+"' WHERE MAKH = '"+txtMaKH.getText().trim()+"'";
                KhachHangDAO.ThemXoaSuaKhachHang(sql);
                dskh = KhachHangDAO.getDSKH();
                laydulieukhachhang(dskh);
                JOptionPane.showMessageDialog(this, "Sửa thành công!", "Thông báo", 2);
            }
            catch(Exception e)
            {
                System.out.println(e);
            }
        }

        //set lại mặc định
        txtMaKH.setEnabled(false);
        txtTenKH.setEnabled(false);
        cbGioiTinh.setEnabled(false);
        dateNgaySinh.setEnabled(false);
        txtDiaChi.setEnabled(false);

        btnSave.setEnabled(false);
        btnThem.setEnabled(true);
        btnXoa.setEnabled(true);
        btnSua.setEnabled(true);
    }//GEN-LAST:event_btnSaveActionPerformed

